// Na POO, uma interface é uma estrutra que define um conjunto de métodos (ASSINATURA DE MÉTODOS) que uma CLASSE deve implementar;

// Ela funciona semelhante a um CONTRATO que garante que as classes que a implementam fornecem funcionalidades específicas.

// Interface é um recurso importante para padronizar o desenvolvimento, utilizado pelos arquitetos de software.

interface CienciaDeDados {
    void analisarDados();
    void processarDados();
}